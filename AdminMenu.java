private List<String> fetchAllProducts() throws SQLException {
    List<String> products = new ArrayList<>();
    Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/ecommercesystem", "root", "");
    String query = "SELECT product_id, name, description, price, last_updated FROM products ORDER BY product_id ASC";
    PreparedStatement ps = conn.prepareStatement(query);
    ResultSet rs = ps.executeQuery();
    while (rs.next()) {
        String product = "Product ID: " + rs.getInt("product_id") +
                         "\nName: " + rs.getString("name") +
                         "\nDescription: " + rs.getString("description") +
                         "\nPrice: $" + rs.getDouble("price") +
                         "\nLast Updated: " + rs.getTimestamp("last_updated");
        products.add(product);
    }
    conn.close();
    return products;
}

