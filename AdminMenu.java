private boolean insertNewCategory(String name) throws SQLException {
    Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/ecommercesystem", "root", "");
    String query = "INSERT INTO categories (name) VALUES (?)";
    PreparedStatement ps = conn.prepareStatement(query);
    ps.setString(1, name);
    int rows = ps.executeUpdate();
    conn.close();
    return rows > 0;
}
private String generateAnalyticsReport() throws SQLException {
    Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/ecommercesystem", "root", "");

    StringBuilder report = new StringBuilder("📊 Analytics Summary:\n\n");

    // Total Orders
    PreparedStatement ps1 = conn.prepareStatement("SELECT COUNT(*) FROM orders");
    ResultSet rs1 = ps1.executeQuery();
    if (rs1.next()) {
        report.append("Total Orders: ").append(rs1.getInt(1)).append("\n");
    }

    // Total Revenue
    PreparedStatement ps2 = conn.prepareStatement("SELECT SUM(amount) FROM payments WHERE status = 'completed'");
    ResultSet rs2 = ps2.executeQuery();
    if (rs2.next()) {
        report.append("Total Revenue: $").append(rs2.getDouble(1)).append("\n");
    }

    // Payment Method Breakdown
    PreparedStatement ps3 = conn.prepareStatement(
        "SELECT method, COUNT(*) AS count FROM payments GROUP BY method"
    );
    ResultSet rs3 = ps3.executeQuery();
    report.append("\nPayment Method Breakdown:\n");
    while (rs3.next()) {
        report.append("- ").append(rs3.getString("method"))
              .append(": ").append(rs3.getInt("count")).append(" payments\n");
    }

    conn.close();
    return report.toString();
}
 
private String generateInventoryTrends() throws SQLException {
    Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/ecommercesystem", "root", "");
    StringBuilder report = new StringBuilder("📦 Inventory Trends:\n\n");

    // Total products
    PreparedStatement ps1 = conn.prepareStatement("SELECT COUNT(*) FROM products");
    ResultSet rs1 = ps1.executeQuery();
    if (rs1.next()) {
        report.append("Total Products: ").append(rs1.getInt(1)).append("\n");
    }

    // Low stock alert (e.g. stock_quantity < 10)
    PreparedStatement ps2 = conn.prepareStatement(
        "SELECT name, stock_quantity FROM products WHERE stock_quantity < 10 ORDER BY stock_quantity ASC"
    );
    ResultSet rs2 = ps2.executeQuery();
    report.append("\n⚠️ Low Stock Products:\n");
    boolean hasLowStock = false;
    while (rs2.next()) {
        hasLowStock = true;
        report.append("- ").append(rs2.getString("name"))
              .append(": ").append(rs2.getInt("stock_quantity")).append(" units\n");
    }
    if (!hasLowStock) {
        report.append("None\n");
    }

    // Recently updated products
    PreparedStatement ps3 = conn.prepareStatement(
        "SELECT name, last_updated FROM products ORDER BY last_updated DESC LIMIT 5"
    );
    ResultSet rs3 = ps3.executeQuery();
    report.append("\n🕒 Recently Updated Products:\n");
    while (rs3.next()) {
        report.append("- ").append(rs3.getString("name"))
              .append(" (Updated: ").append(rs3.getTimestamp("last_updated")).append(")\n");
    }

    conn.close();
    return report.toString();
}

