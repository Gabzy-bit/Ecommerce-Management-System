private List<String> fetchAllOrders() throws SQLException {
    List<String> orders = new ArrayList<>();
    Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/ecommercesystem", "root", "");
    String query = "SELECT order_id, user_id, order_date, status FROM orders ORDER BY order_date DESC";
    PreparedStatement ps = conn.prepareStatement(query);
    ResultSet rs = ps.executeQuery();
    while (rs.next()) {
        String order = "Order ID: " + rs.getInt("order_id") +
                       "\nUser ID: " + rs.getString("user_id") +
                       "\nDate: " + rs.getTimestamp("order_date") +
                       "\nStatus: " + rs.getString("status");
        orders.add(order);
    }
    conn.close();
    return orders;
}

private boolean updateOrderStatus(int orderId, String newStatus) throws SQLException {
    Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/ecommercesystem", "root", "");
    String query = "UPDATE orders SET status = ? WHERE order_id = ?";
    PreparedStatement ps = conn.prepareStatement(query);
    ps.setString(1, newStatus);
    ps.setInt(2, orderId);
    int rows = ps.executeUpdate();
    conn.close();
    return rows > 0;
}

