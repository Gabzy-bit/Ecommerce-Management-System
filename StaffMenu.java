private void viewShippingInfo(int orderId) throws SQLException {
    Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/ecommercesystem", "root", "");
    String query = "SELECT * FROM shipping WHERE order_id = ?";
    PreparedStatement ps = conn.prepareStatement(query);
    ps.setInt(1, orderId);
    ResultSet rs = ps.executeQuery();
    if (rs.next()) {
        String info = "Shipping ID: " + rs.getInt("shipping_id") +
                      "\nAddress: " + rs.getString("address") +
                      "\nCity: " + rs.getString("city") +
                      "\nPostal Code: " + rs.getString("postal_code") +
                      "\nCountry: " + rs.getString("country") +
                      "\nDate Shipped: " + rs.getTimestamp("date_shipped");
        JOptionPane.showMessageDialog(null, info, "Shipping Info", JOptionPane.INFORMATION_MESSAGE);
    } else {
        JOptionPane.showMessageDialog(null, "No shipping info found for this order.");
    }
    conn.close();
}

private void editShippingInfo(int orderId) throws SQLException {
    String address = JOptionPane.showInputDialog(null, "Enter address:");
    String city = JOptionPane.showInputDialog(null, "Enter city:");
    String postal = JOptionPane.showInputDialog(null, "Enter postal code:");
    String country = JOptionPane.showInputDialog(null, "Enter country:");

    if (address != null && city != null && postal != null && country != null) {
        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/ecommercesystem", "root", "");

        // Check if shipping record exists
        String checkQuery = "SELECT shipping_id FROM shipping WHERE order_id = ?";
        PreparedStatement checkPs = conn.prepareStatement(checkQuery);
        checkPs.setInt(1, orderId);
        ResultSet rs = checkPs.executeQuery();

        if (rs.next()) {
            // Update existing record
            String updateQuery = "UPDATE shipping SET address = ?, city = ?, postal_code = ?, country = ? WHERE order_id = ?";
            PreparedStatement ps = conn.prepareStatement(updateQuery);
            ps.setString(1, address);
            ps.setString(2, city);
            ps.setString(3, postal);
            ps.setString(4, country);
            ps.setInt(5, orderId);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Shipping info updated.");
        } else {
            // Insert new record
            String insertQuery = "INSERT INTO shipping (order_id, address, city, postal_code, country) VALUES (?, ?, ?, ?, ?)";
            PreparedStatement ps = conn.prepareStatement(insertQuery);
            ps.setInt(1, orderId);
            ps.setString(2, address);
            ps.setString(3, city);
            ps.setString(4, postal);
            ps.setString(5, country);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Shipping info added.");
        }

        conn.close();
    }
}

private void markOrderAsShipped(int orderId) throws SQLException {
    Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/ecommercesystem", "root", "");

    // Update orders table
    String updateOrder = "UPDATE orders SET status = 'shipped' WHERE order_id = ?";
    PreparedStatement ps1 = conn.prepareStatement(updateOrder);
    ps1.setInt(1, orderId);
    ps1.executeUpdate();

    // Update shipping table
    String updateShipping = "UPDATE shipping SET date_shipped = NOW() WHERE order_id = ?";
    PreparedStatement ps2 = conn.prepareStatement(updateShipping);
    ps2.setInt(1, orderId);
    ps2.executeUpdate();

    conn.close();
    JOptionPane.showMessageDialog(null, "Order marked as shipped.");
}


