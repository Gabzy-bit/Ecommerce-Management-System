    private void jButtonTrackShippingActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonTrackShippingActionPerformed
        // TODO add your handling code here:
        
    String orderId = JOptionPane.showInputDialog(this, "Enter Order ID to track shipping:");
    if (orderId == null || orderId.trim().isEmpty()) return;

    try (Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/ecommercesystem", "root", "");
         PreparedStatement stmt = conn.prepareStatement(
             "SELECT address, city, country, shipped_date, delivery_date FROM shipping WHERE order_id = ?")) {

        stmt.setInt(1, Integer.parseInt(orderId));
        ResultSet rs = stmt.executeQuery();

        if (rs.next()) {
            String address = rs.getString("address");
            String city = rs.getString("city");
            String country = rs.getString("country");
            Timestamp shipDate = rs.getTimestamp("shipped_date");
            Timestamp deliveryDate = rs.getTimestamp("delivery_date");

            String message = String.format(
                "Shipping Info:\nAddress: %s\nCity: %s\nCountry: %s\nShipped: %s\nDelivery: %s",
                address, city, country,
                shipDate != null ? shipDate.toString() : "Not yet shipped",
                deliveryDate != null ? deliveryDate.toString() : "Not yet delivered"
            );

            JOptionPane.showMessageDialog(this, message);
        } else {
            JOptionPane.showMessageDialog(this, "No shipping info found for Order ID: " + orderId);
        }

    } catch (SQLException | NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }


    }//GEN-LAST:event_jButtonTrackShippingActionPerformed

