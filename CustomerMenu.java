    private void jButtonAddToCartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddToCartActionPerformed
                                                    
    int selectedRow = productTable.getSelectedRow();
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(null, "Please select a product to add.");
        return;
    }

    String productId = productTable.getValueAt(selectedRow, 0).toString();
    int quantity = 1;

    System.out.println("Current user ID: " + currentUserId);

    if (currentUserId == null || currentUserId.isEmpty()) {
        JOptionPane.showMessageDialog(null, "User ID is missing. Please log in again.");
        return;
    }

    try {
        Class.forName("com.mysql.cj.jdbc.Driver");
        Connection con = DriverManager.getConnection(dblink, user, password);
        String sql = "INSERT INTO cart (user_id, product_id, quantity) VALUES (?, ?, ?)";
        PreparedStatement pst = con.prepareStatement(sql);
        pst.setString(1, currentUserId);
        pst.setString(2, productId);
        pst.setInt(3, quantity);

        int rowsInserted = pst.executeUpdate();
        if (rowsInserted > 0) {
            JOptionPane.showMessageDialog(null, "Product added to cart!");
        }

        pst.close();
        con.close();
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(null, "Error adding product to cart.");
    }




    


    }//GEN-LAST:event_jButtonAddToCartActionPerformed

