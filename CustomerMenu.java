private void loadCartItems() {
    DefaultTableModel model = (DefaultTableModel) cartTable.getModel();
    model.setRowCount(0); // clear existing rows

    try {
        Class.forName("com.mysql.cj.jdbc.Driver");
        Connection con = DriverManager.getConnection(dblink, user, password);

        String sql = "SELECT p.product_id, p.name, p.price, c.quantity " +
                     "FROM cart c JOIN products p ON c.product_id = p.product_id " +
                     "WHERE c.user_id = ?";
        PreparedStatement pst = con.prepareStatement(sql);
        pst.setString(1, currentUserId);

        ResultSet rs = pst.executeQuery();
        while (rs.next()) {
            String productId = rs.getString("product_id");
            String name = rs.getString("name");
            double price = rs.getDouble("price");
            int quantity = rs.getInt("quantity");

            model.addRow(new Object[]{productId, name, price, quantity});
        }

        rs.close();
        pst.close();
        con.close();
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(null, "Error loading cart items.");
    }
}

private void clearCart() {
    try {
        Class.forName("com.mysql.cj.jdbc.Driver");
        Connection con = DriverManager.getConnection(dblink, user, password);
        String sql = "DELETE FROM cart WHERE user_id = ?";
        PreparedStatement pst = con.prepareStatement(sql);
        pst.setString(1, currentUserId);

        int rowsDeleted = pst.executeUpdate();
        if (rowsDeleted > 0) {
            JOptionPane.showMessageDialog(null, "Cart cleared successfully.");
        } else {
            JOptionPane.showMessageDialog(null, "Your cart was already empty.");
        }

        pst.close();
        con.close();

        // Refresh the cart table to show it's empty
        loadCartItems();

    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(null, "Error clearing cart.");
    }
}

private int getStock(Connection conn, int productId) throws SQLException {
    PreparedStatement ps = conn.prepareStatement("SELECT stock_quantity FROM products WHERE product_id = ?");
    ps.setInt(1, productId);
    ResultSet rs = ps.executeQuery();
    return rs.next() ? rs.getInt("stock_quantity") : 0;
}

private void updateStock(Connection conn, int productId, int newStock) throws SQLException {
    PreparedStatement ps = conn.prepareStatement("UPDATE products SET stock_quantity = ? WHERE product_id = ?");
    ps.setInt(1, newStock);
    ps.setInt(2, productId);
    ps.executeUpdate();
}

private void insertOrder(Connection conn, String userId) throws SQLException {
    PreparedStatement ps = conn.prepareStatement("INSERT INTO orders (user_id, order_date, status) VALUES (?, NOW(), 'pending')");
    ps.setString(1, userId);
    ps.executeUpdate();
}


private List<String> fetchPaymentsHistory(String userId) throws SQLException {
    List<String> payments = new ArrayList<>();
    Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/ecommercesystem", "root", "");

    // Join orders and payments to match user_id
    String query = "SELECT p.payment_id, p.order_id, p.amount, p.payment_date, p.method, p.status " +
                   "FROM payment p JOIN orders o ON p.order_id = o.order_id " +
                   "WHERE o.user_id = ? ORDER BY p.payment_date DESC";

    PreparedStatement ps = conn.prepareStatement(query);
    ps.setString(1, userId);
    ResultSet rs = ps.executeQuery();

    while (rs.next()) {
        String payment = "Payment ID: " + rs.getInt("payment_id") +
                         "\nOrder ID: " + rs.getInt("order_id") +
                         "\nAmount: $" + rs.getDouble("amount") +
                         "\nDate: " + rs.getTimestamp("payment_date") +
                         "\nMethod: " + rs.getString("method") +
                         "\nStatus: " + rs.getString("status");
        payments.add(payment);
    }

    conn.close();
    return payments;
}

