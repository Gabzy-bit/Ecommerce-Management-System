    private void jButtonMakePaymentsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonMakePaymentsActionPerformed
        // TODO add your handling code here:
         // Step 1: Ask for phone number
    String phone = JOptionPane.showInputDialog(this, "Enter your phone number:");
    if (phone == null || phone.trim().isEmpty()) return;

    // Step 2: Ask for amount
    String amountStr = JOptionPane.showInputDialog(this, "Enter amount to pay (UGX):");
    if (amountStr == null || amountStr.trim().isEmpty()) return;

    int amount;
    try {
        amount = Integer.parseInt(amountStr);
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Invalid amount format.");
        return;
    }
   // Step 3: Choose payment method
    String[] options = {"Airtel Pay", "MoMo Pay", "PayPal"};
    String choice = (String) JOptionPane.showInputDialog(this, "Choose payment method:", "Payment",
            JOptionPane.PLAIN_MESSAGE, null, options, options[0]);

    if (choice == null) return;

    // Step 4: Call the correct method
    
switch (choice) {
    case "Airtel Pay":
        PaymentMethods.initiateAirtelPayment(phone, amount);
        break;
    case "MoMo Pay":
        PaymentMethods.initiateMoMoPayment(phone, amount);
        break;
    case "PayPal":
        PaymentMethods.redirectToPayPal(phone, amount); // using phone as email for now
        break;
}
String receipt = String.format(
    "✅ Payment Successful!\n\nMethod: %s\nPhone: %s\nAmount: UGX %,d\n\nThank you for using our service.",
    choice, phone, amount
);
JOptionPane.showMessageDialog(this, receipt, "Payment Receipt", JOptionPane.INFORMATION_MESSAGE);



    }//GEN-LAST:event_jButtonMakePaymentsActionPerformed

