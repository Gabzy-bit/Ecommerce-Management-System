    private void jButtonPlaceOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPlaceOrderActionPerformed
        // TODO add your handling code here:
       
    DefaultTableModel model = (DefaultTableModel) cartTable.getModel();
    int rowCount = model.getRowCount();

    if (rowCount == 0) {
        JOptionPane.showMessageDialog(this, "Cart is empty.");
        return;
    }

    try (Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/ecommercesystem", "root", "")) {

        for (int i = 0; i < rowCount; i++) {
            int productId = Integer.parseInt(model.getValueAt(i, 0).toString());
            String name = model.getValueAt(i, 1).toString();
            int quantity = Integer.parseInt(model.getValueAt(i, 3).toString());

            int stock = getStock(conn, productId);
            if (stock < quantity) {
                JOptionPane.showMessageDialog(this, "Insufficient stock for: " + name);
                return;
            }
        }

        for (int i = 0; i < rowCount; i++) {
            int productId = Integer.parseInt(model.getValueAt(i, 0).toString());
            int quantity = Integer.parseInt(model.getValueAt(i, 3).toString());

            int stock = getStock(conn, productId);
            int newStock = stock - quantity;

            updateStock(conn, productId, newStock);
        }

        insertOrder(conn, currentUserId); // Logs the order in your orders table
        model.setRowCount(0); // Clears the cart table
        JOptionPane.showMessageDialog(this, "Order placed successfully!");

    } catch (Exception ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error placing order.");
    }




   
    }//GEN-LAST:event_jButtonPlaceOrderActionPerformed

    private void btnSearchProductsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchProductsActionPerformed
        // TODO add your handling code here:
    
    // Create a table model with updated columns
    DefaultTableModel model = new DefaultTableModel() {
        @Override
        public boolean isCellEditable(int row, int column) {
            return false; // Prevent editing
        }
    };
    model.setColumnIdentifiers(new String[] {
        "Product ID", "Name", "Description", "Price", "Stock", "Last Updated", "Supplier ID"
    });

    try {
        Connection con = DBConnection.getConnection();
        String query = "SELECT product_id, name, description, price, stock_quantity, last_updated, supplier_id FROM products";
        PreparedStatement pst = con.prepareStatement(query);
        ResultSet rs = pst.executeQuery();

        while (rs.next()) {
            int id = rs.getInt("product_id");
            String name = rs.getString("name");
            String desc = rs.getString("description");
            double price = rs.getDouble("price");
            int stock = rs.getInt("stock_quantity");
            Timestamp updated = rs.getTimestamp("last_updated");
            int supplier = rs.getInt("supplier_id");

            model.addRow(new Object[] { id, name, desc, price, stock, updated, supplier });
        }

        productTable.setModel(model);
        productTable.setRowHeight(25);
        productTable.getTableHeader().setFont(new java.awt.Font("SansSerif", java.awt.Font.BOLD, 14));
        productTable.setFont(new java.awt.Font("SansSerif", java.awt.Font.PLAIN, 13));

    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error loading products.");
    }


    

    }//GEN-LAST:event_btnSearchProductsActionPerformed

